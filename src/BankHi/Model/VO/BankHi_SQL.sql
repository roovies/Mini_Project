-- SH은행
CREATE TABLE SHBANK(
    SH_ID VARCHAR2(20) NOT NULL CONSTRAINT FK_SH_ID REFERENCES MEMBER(MEMBER_ID),
    SH_NAME VARCHAR2(20) NOT NULL,
    SH_NUM VARCHAR2(14) CONSTRAINT PK_SH_NUM PRIMARY KEY,
    SH_PW VARCHAR2(4) NOT NULL,
    SH_BALANCE NUMBER DEFAULT 0 NOT NULL,
    LOAN_YN VARCHAR2(1) DEFAULT 'Y' NOT NULL CHECK(LOAN_YN IN('Y', 'N')),
    LOAN_MONEY NUMBER DEFAULT 0 NOT NULL,
    LOAN_LIMIT NUMBER DEFAULT 100000000,
    ENROLL_DATE DATE DEFAULT SYSDATE
);
COMMENT ON COLUMN SHBANK.SH_ID IS '아이디';
COMMENT ON COLUMN SHBANK.SH_NAME IS '이름';
COMMENT ON COLUMN SHBANK.SH_NUM IS '계좌번호';
COMMENT ON COLUMN SHBANK.SH_PW IS '비밀번호';
COMMENT ON COLUMN SHBANK.SH_BALANCE IS '잔액';
COMMENT ON COLUMN SHBANK.LOAN_YN IS '대출가능여부';
COMMENT ON COLUMN SHBANK.LOAN_MONEY IS '대출금액';
COMMENT ON COLUMN SHBANK.LOAN_LIMIT IS '남은대출한도';
COMMENT ON COLUMN SHBANK.ENROLL_DATE IS '등록일';


