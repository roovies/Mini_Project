CREATE TABLE MEMBER(
    MEMBER_ID VARCHAR2(20) NOT NULL CONSTRAINT PK_MEMBER_ID PRIMARY KEY,
    MEMBER_PW VARCHAR2(20) NOT NULL,
    MEMBER_NAME VARCHAR2(20) NOT NULL,
    MEMBER_GENDER VARCHAR2(3) NOT NULL CHECK(MEMBER_GENDER IN('남','여')),
    MEMBER_SSN VARCHAR2(14) NOT NULL,
    MEMBER_PHONE VARCHAR2(13) NOT NULL,
    MEMBER_EMAIL VARCHAR2(30),
    MEMBER_ADDR VARCHAR2(100) NOT NULL,
    MEMBER_BANK VARCHAR2(20) NOT NULL,
    ENROLL_DATE DATE DEFAULT SYSDATE
);

COMMENT ON TABLE MEMBER IS '회원정보';
COMMENT ON COLUMN MEMBER.MEMBER_ID IS '아이디';
COMMENT ON COLUMN MEMBER.MEMBER_PW IS '비밀번호';
COMMENT ON COLUMN MEMBER.MEMBER_NAME IS '이름';
COMMENT ON COLUMN MEMBER.MEMBER_GENDER IS '성별';
COMMENT ON COLUMN MEMBER.MEMBER_SSN IS '주민등록번호';
COMMENT ON COLUMN MEMBER.MEMBER_PHONE IS '연락처';
COMMENT ON COLUMN MEMBER.MEMBER_EMAIL IS '이메일';
COMMENT ON COLUMN MEMBER.MEMBER_ADDR IS '주소';
COMMENT ON COLUMN MEMBER.MEMBER_BANK IS '은행명';
COMMENT ON COLUMN MEMBER.ENROLL_DATE IS '가입일';




